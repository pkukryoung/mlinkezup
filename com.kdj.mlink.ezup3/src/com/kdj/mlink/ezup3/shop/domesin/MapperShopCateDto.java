package com.kdj.mlink.ezup3.shop.domesin;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class MapperShopCateDto {

	 @JsonProperty("cid")
     private String cid;
	 @JsonProperty("shopcd")
     private String shopcd;

     @JsonProperty("esm_auction")
     private  String esm_auction;

     @JsonProperty("esm_gmarket")
     private  String esm_gmarket ;


     public String getCid() {
		return cid;
	}



	public void setCid(String cid) {
		this.cid = cid;
	}


	public String getShopcd() {
		return shopcd;
	}


	public void setShopcd(String shopcd) {
		this.shopcd = shopcd;
	}

	public String getEsm_auction() {
		return esm_auction;
	}

	public void setEsm_auction(String esm_auction) {
		this.esm_auction = esm_auction;
	}


	public String getEsm_gmarket() {
		return esm_gmarket;
	}


	public void setEsm_gmarket(String esm_gmarket) {
		this.esm_gmarket = esm_gmarket;
	}

	
	
	
	

	@Override
     public String toString() {
         return "CafeCate{" +
                 "cid='" + cid + '\'' +
                 ", shopcd='" + shopcd + '\'' +
                 ", esm_auction='" + esm_auction + '\'' +
                 ", esm_gmarket='" + esm_gmarket + '\'' +
                 '}';
     }
     
}
